console.log(x);

x -= y; // Operador de asignación de resta
console.log(x);

x *= y; // Operador de asignación de multiplicación
console.log(x);

x /= y; // Operador de asignación de división
console.log(x);

x %= y; // Operador de asignación de residuo
console.log(x);

x **=y; // Operador de asignación de exponenciación
